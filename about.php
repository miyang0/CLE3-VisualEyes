    </li>
    <li>Entrepreneurship: Project-based work; I can estimate deliverables and deadlines, and I am able to estimate the
        entire process and adhere to it, so that the client knows what to expect.
    </li>
    <li>Entrepreneurship: Entrepreneurial attitude; I make an active contribution to the team.</li>
    <li>Entrepreneurship: Entrepreneurial skills; I expand my professional knowledge by attending an industry-related
