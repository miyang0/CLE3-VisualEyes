<section id="reasons">
<h2>Reasons</h2>
<p>Originally, this was an assignment we were given by our study program. Each group received one random target group
    and one random activity to improve for this target group. We were assigned people with visual impairments and
    shopping. But we really do want to help people. At this moment, there are probably already better assistive devices
    available than what we can create in such a short time. But we are learning from this. Because we are doing this
    now, we can make something even better in the future.</p>
