    with JavaScript, learned to design ecologically, and learned more about what it is like to have a disability. The
    learning objectives we wanted to achieve this period are based on entrepeneurship and involvement. These are all the
    learning objectives:</p>
<ul>
    <li>Entrepreneurship: Communication; I can deliver presentations and documentation tailored to the target audience
